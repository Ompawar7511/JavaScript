//Premative data type in javaScript
// 1.Number
// 2.String
// 3.Boolean
// 4.Undefined
// 5.Null
// 6.array
// 7.object (ke And Value pair)
// 8.Constant
//number(It store Numeric Value )
let num1=10;
console.log("num1=",num1);
console.log("This is type of num1=",typeof num1);
//string(It store character value )
let name="Rutik petkar"
console.log("name=",name);
console.log("This is the dataType of name=",typeof name);
//boolean(It store only true or false value in the form of 0 or 1)
let val=true;
console.log("val",val);
console.log("This is the data type of val=",typeof val);
//Undefine (If you can't Declare the Variable then It Show Undefine DataType )
let temp;
console.log("temp=",temp);
console.log("This is the DataType of temp=",typeof temp);
//null(It store null Value (It show dataType in the form of object(default)))
let name1=null;
console.log("name1",name1);
console.log("This is dataType of name1=",typeof name1);
